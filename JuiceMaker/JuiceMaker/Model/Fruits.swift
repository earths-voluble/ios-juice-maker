import Foundation

class Fruits {

    var strawberry : Int
    var banana : Int
    var pineapple : Int
    var kiwi : Int
    var mango : Int
    
    init(strawberry: Int = 10, banana: Int = 10, pineapple: Int = 10, kiwi: Int = 10, mango: Int = 10) {
        self.strawberry = strawberry
        self.banana = banana
        self.pineapple = pineapple
        self.kiwi = kiwi
        self.mango = mango
    }
}
